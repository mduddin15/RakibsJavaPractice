package Basic.ProgramizProblems.ControlFlow;

public class IfElseCheck {

    public static void main(String[] args) {

        int x = 10;

        if (x > 0) {
            System.out.println("Hello");
        } else if (x < 100) {
            System.out.println("Hi");
        } else {
            System.out.println("Howdy");
        }

    }
}