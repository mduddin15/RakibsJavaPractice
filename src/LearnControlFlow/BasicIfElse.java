package LearnControlFlow;

public class PrintStar {

    public static void main(String[] args) {

        // Print 100 *
        for (int k = 1; k <= 100; k++) {
            System.out.print('*');
        }

        //Print multiples of 6.
        System.out.println();
        for (int i = 1; i <= 10; i++) {
            System.out.print(i * 6 + " ");
        }
    }
}